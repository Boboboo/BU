package com.kcc.entity;

public class Hurricane {
	int h_id;
	String h_day;
	String h_time;
	String h_identifier;
	String h_state;
	String h_latitude;
	String h_longitude;
	int h_max_speed;
	String h_number;
	String h_name;
	
	public Hurricane() {
		
	}

	public int getH_id() {
		return h_id;
	}

	public void setH_id(int h_id) {
		this.h_id = h_id;
	}

	public String getH_day() {
		return h_day;
	}

	public void setH_day(String h_day) {
		this.h_day = h_day;
	}

	public String getH_time() {
		return h_time;
	}

	public void setH_time(String h_time) {
		this.h_time = h_time;
	}

	public String getH_identifier() {
		return h_identifier;
	}

	public void setH_identifier(String h_identifier) {
		this.h_identifier = h_identifier;
	}

	public String getH_state() {
		return h_state;
	}

	public void setH_state(String h_state) {
		this.h_state = h_state;
	}

	public String getH_latitude() {
		return h_latitude;
	}

	public void setH_latitude(String h_latitude) {
		this.h_latitude = h_latitude;
	}

	public String getH_longitude() {
		return h_longitude;
	}

	public void setH_longitude(String h_longitude) {
		this.h_longitude = h_longitude;
	}

	public int getH_max_speed() {
		return h_max_speed;
	}

	public void setH_max_speed(int h_max_speed) {
		this.h_max_speed = h_max_speed;
	}

	public String getH_number() {
		return h_number;
	}

	public void setH_number(String h_number) {
		this.h_number = h_number;
	}

	public String getH_name() {
		return h_name;
	}

	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	
}
